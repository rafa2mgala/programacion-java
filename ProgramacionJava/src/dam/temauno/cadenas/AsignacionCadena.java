package dam.temauno.cadenas;
/**
 * Clase de ejemplo para asignación de cadenas
 * @author rafa
 *
 */
public class AsignacionCadena {

	public static void main(String[] args) {		
		//inicialización como tipo de datos primitivo
		String cadenaUno = "Cadena inicializada como tipo primitivo";
		//inicialización como tipo de dato objeto
		String cadenaDos = new String("Cadena inicializada como tipo objeto");
		//se muestra el valor de las variables por consola
		System.out.println(cadenaUno);
		System.out.println(cadenaDos);
	}

}
